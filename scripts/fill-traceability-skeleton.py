def main() -> None:
    with SEMI.open("r", encoding="utf-8-sig") as f:
        semi_rows = list(csv.DictReader(f))
    semi_by_id = {r["需求 ID"]: r for r in semi_rows}

    with SKELETON.open("r", encoding="utf-8-sig") as f:
        sk_rows = list(csv.DictReader(f))

    fieldnames = [
        "需求 ID",
        "模块",
        "需求名称",
        "类型",
        "PRD 依据",
        "描述",
        "前置条件",
        "验收标准（可测）",
        "优先级",
        "状态",
        "测试用例 ID",
        "依赖/备注",
    ]

    out: list[dict[str, str]] = []
    for row in sk_rows:
        rid = row["需求 ID"]
        merged = {**row}
        semi = semi_by_id.get(rid, {})

        if rid in ENRICH:
            for k, v in ENRICH[rid].items():
                merged[k] = v
        else:
            # semi-auto 优先补名称与 PRD
            if semi.get("需求名称"):
                merged["需求名称"] = semi["需求名称"]
            if semi.get("类型"):
                merged["类型"] = semi["类型"]
            if semi.get("PRD 依据"):
                merged["PRD 依据"] = semi["PRD 依据"]
            fill = default_fill(
                rid,
                merged.get("模块") or "",
                merged.get("需求名称"),
                merged.get("PRD 依据"),
            )
            for k, v in fill.items():
                if not (merged.get(k) or "").strip():
                    merged[k] = v

        if not (merged.get("状态") or "").strip():
            merged["状态"] = "草稿"
        out.append(merged)

    with OUT.open("w", encoding="utf-8-sig", newline="") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames, quoting=csv.QUOTE_MINIMAL)
        w.writeheader()
        w.writerows(out)

    print(f"Wrote {len(out)} rows to {OUT}")


if __name__ == "__main__":
    main()
